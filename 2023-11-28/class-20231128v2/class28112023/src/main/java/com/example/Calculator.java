// This is a example of the class Calculator in Java
// The class represent a simple calculator with methods of sum and subtraction  
  

package com.example; 

// class definition  
public class Calculator {
    

    // Method to execute the adition between two numbers 
    public int sum(int a, int b){
        return a + b; 
    }

    // Method to execute the subtraction between two numbers 
    public int subtract (int a, int b){
        return a - b; 
    }

    public int divide (int a, int b) {
        return a / b; 
    }
}
